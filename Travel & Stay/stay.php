<h1>Eating</h1>
<img id="l1" class="ima"  src="Rline.gif"></img>
<h3>
<ul type="square">
<li>
Try not to eat on the public street or sit at open roadside tea stalls.</li> 
<br> 
<li>If you have to purchase anything to drink or eat from outside, bring them back to your room as far as possible.</li> 
<br>
<li>Ladies, especially, should not under any condition sit at open roadside tea stalls.</li> 
<br>

</h3>




<h1>Precautions In Temples </h1>
<img id="l1" class="ima"  src="Rline.gif"></img>
<h3>
<ul type="square">
<li>
Photography is prohibited in many temples in Vrindavan. Do check first as many temple caretakers are known to break cameras.</li><br> 

<li>Do not wear shoes when visiting temples or doing parikramas.Do not carry your shoes in a bag into any temple of the Lord. Many temples do not even permit wearing socks in the temple.</li> 
<br>
<li>Carry small coins to give as donations to women/ men minding your shoes for you while you visit temples.</li>
<br>

<li>Beware of touts, pickpockets, and so called “guides.” If you are harassed by aggressive “pandas” it is best to either ignore them or give a small donation and move on.Do not get into an argument with them.</li>

</h3>


<br>
