<h2>Traveling to India for Vrindavan</h2>
<img id="l1" class="ima1"  style="width:400" src="Rline.gif"></img>
<h3>The most convenient airport for coming to Vrindavan is Delhi (160 Kilometres). The two other most common airports are Mumbai and Kolkata. Trains are available from both Mumbai and Kolkata to get you to Vrindavan.</h3>

<br>


<h2>From Delhi</h2> 
<img id="l1" class="ima1"  src="Rline.gif"></img>
<h3>
<ul type="circle">
<br>
<li>From Airport</li>
<br>
<ul type="square">
<li>From Delhi airport you can get a taxi directly to Vrindavan.</li>
<li>It is highly recommended that the fare be fixed always before hand.</li>
<li>Via a travel agent in Vrindavan you can arrange for a taxi from Vrindavan to collect you.</li>
<li>You should expect to pay about INR 1900 for a non AC taxi (Indica) to Vrindavan and about INR 2100 for an AC taxi.</li>
<li>If you have a lot of luggage, it would be better to take a SUV (Sumo or Qualis) which would cost a bit more.</li>

</ul>
<br>
<li>By Train</li>
<ul type="square">
<br>
<li>You can also get trains from the center of Delhi to Mathura.</li>
<li>You will then need to get an Auto Rickshaw (tuk tuk) or taxi to Vrindavan.</li>
<li>The Auto Rickshaw (tuk tuk) from Mathura should be no more than INR 100 (again price to be fixed before hand)</li> 
<li>Taxi would cost approx INR 400</li>
</ul>
<br>
<li>By Bus</li>
<ul type="square">
<br>
<li>Buses are also available from Delhi</li>
<li>Some buses come directly into Vrindavan, otherwise you will have to get off the bus at Mathura and then get an auto rickshaw to Vrindavan.</li>
<li>State run and some private owned buses start from Sarai Kalekhan Inter State Bus Terminal near Hazrat Nizamuddin Railway Station in Delhi.</li>
<li>Buses to Vrindavan or Mathura also depart from the Interstate Bus Terminal near Kashmiri Gate, in Old Delhi</li>
<li>Also from Ashram bus stop in the southeast part of Delhi.</li>
</ul>
</ul>



</h3>

<br>
<div class="a1">
<h2>From Mumbai</h2> 
<img id="l1" class="ima1"  src="Rline.gif"></img>
<br>
<h3>
<ul type="square">

<li>A number of good trains direct to Mathura (Vrindavan).</li>
<li>Second Class Sleeper Ticket will cost about INR 500.</li>
<li>3 tier AC ticket will cost about INR 1200.</li>
<li>The Golden Temple or Paschim Express are both good trains.</li>
<li>August Kranti Express is also a fully airconditioned train and a 3 tier AC ticket would cost about INR 1500 (including breakfast, dinner and blanket etc.</li>
<li>Garib Rath is another fully AC train and would cost much less.</li>
</ul>



</h3></div>


<div class="a2">
<h2>From Kolkata</h2>
<img id="l1" class="ima1"  src="Rline.gif"></img>
<br>
<h3>
<ul type="square">
<li>As Mumbai there are no good trains direct to Mathura (Vrindavan).</li>
<li>You can get the Kalka Mail train to either Tundla or Aligarh and then get a taxi or bus to Mathura/Vrindavan</li>
<li>Second Class Sleeper Ticket will cost about INR 500 and 3 tier AC will cost about INR 1200.</li>
<li> A non AC taxi from Tundla or Aligarh will cost about INR 1000.</li>
<li>Another option is to get a train to Delhi and then come to Vrindavan as detailed above.</li>


</h3></div>
<br>

<div class="a3">
<h2>From Chennai</h2> 
<img id="l1" class="ima1"  src="Rline.gif"></img>
<h3>
<ul type="square">
<br>
<li>From Chennai the train journey would be rather long (about 33 hours).</li>
<li>There are a couple of trains (Grand Trunk, Andaman Express) to Mathura (Vrindavan)<br> and from there one could take an Auto Rickshaw or Taxi to Vrindavan.
</li>
<li>Taking a flight to Delhi is a better option.</li>
</ul>
