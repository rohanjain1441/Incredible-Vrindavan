<h2>Buses</h2> 
<img id="l1" class="ima1"  src="Rline.gif"></img>

<br>
<ul type="square">
<h3>
<li>There is a daily bus service to Delhi from the bus depot in Vrindavan. The same bus also stops at the bus stop opposite the ISCKON Temple.  If you want to have a comfortable seat, it is advisable to go to the bus depot. </li>
<br>
<li>Fare: Rs.85/-(approx) per person (The buses are with non reclining seats, non AC and are State run. However, they have a pretty reliable service.</li>
<br>
<li>There is a direct bus to Jaipur at 5.45 am from the Vrindavan bus stand.</li>
<br>
<li>You can also get buses to Agra (1 hr). There is an overnight bus, with thin seats, that goes to Haridwar at around 8 pm.</li>
<br>
<li>The buses to Govardhana, Radha Kunda, Delhi and Varsana can be taken from the Vrindavan Bus Depot or the Bus stop  in front of the ISKCON temple.</li>
<br>
<li>The Braja Darshan departs from the bus station at 8 am and goes to Nandagram, Varsana, Govardhana and Radha Kunda. It is a economical way to visit all these places.</li>
<br>
<li>Bus Times:</li>
